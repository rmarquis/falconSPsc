InputFunctionType FindFunctionFromString (char* str)
{
InputFunctionType retval = NULL;
int i = 0;

   while (UserFunctionList[i].theFunc)
   {
      if (strcmp(UserFunctionList[i].funcName, str) == 0)
      {
         retval = UserFunctionList[i].theFunc;
         break;
      }

      i++;
   }

   return (retval);
}
 
