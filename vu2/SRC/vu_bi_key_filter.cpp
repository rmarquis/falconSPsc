//-----------------------------------------------------------------------------
// VuBiKeyFilter -- abstract class
//-----------------------------------------------------------------------------

VuBiKeyFilter::~VuBiKeyFilter()
{
	// empty
}

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

ulong VuBiKeyFilter::Key1(VuEntity* ent)
{
#ifdef VU_GRID_TREE_Y_MAJOR
	return CoordToKey1(ent->YPos());
#else
	return CoordToKey1(ent->XPos());
#endif
}

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

ulong VuBiKeyFilter::Key2(VuEntity* ent)
{
#ifdef VU_GRID_TREE_Y_MAJOR
	return CoordToKey2(ent->XPos());
#else
	return CoordToKey2(ent->YPos());
#endif
}

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

ulong VuBiKeyFilter::Key(VuEntity* ent)
{
	return Key2(ent);
}

