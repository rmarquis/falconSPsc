VuLifoQueue::VuLifoQueue(VuFilter* filter) : VuFilteredList(filter)
{
	// empty
}

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

VuLifoQueue::~VuLifoQueue()
{
	// empty
}

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

int 
VuLifoQueue::Type()
{
	return VU_LIFO_QUEUE_COLLECTION;
}

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

VuEntity*
VuLifoQueue::Peek()
{
	return head_->entity_;
}

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

VuEntity*
VuLifoQueue::Pop()
{
	VuEntity* retval = Peek();
	Remove(retval);
	return retval;
}

